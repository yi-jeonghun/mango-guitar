		var new_base = '';
		if(up_down == 'up'){
			new_root = self.GetNextChord(root, true);
			if(base != ''){
				new_base = self.GetNextChord(base, true);
			}
		}else if(up_down == 'down'){
			new_root = self.GetNextChord(root, false);
			if(base != ''){
				new_base = self.GetNextChord(base, false);
			}
		}

		var new_chord = new_root + type;
		if(base != ''){
			new_chord += '/' + new_base;
		}
		return new_chord;
